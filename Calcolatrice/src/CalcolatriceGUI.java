    private void charEvent(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_charEvent
      if(cellaUno==0){
            cellaUno=Integer.parseInt(jLabel1.getText());
            System.out.println(cellaUno);
            segno=((JButton)evt.getSource()).getText();
            System.out.println(segno);
            jLabel1.setText("");
            jLabel2.setText(cellaUno+""+segno);
        }else{
          
          cellaDue=Integer.parseInt(jLabel1.getText());
          int risultato;
          switch(segno){
              case "/": jLabel1.setText(""+(cellaUno/cellaDue)); break;
              case "x": jLabel1.setText(""+(cellaUno*cellaDue)); break;
              case "+": jLabel1.setText(""+(cellaUno+cellaDue)); break;
              case "-": jLabel1.setText(""+(cellaUno-cellaDue)); break;       
          }
          segno="";
          cellaUno=0; cellaDue=0;
          jLabel2.setText("");
      }
    }//GEN-LAST:event_charEvent

